    public String ausgabe(ResultSet rs) throws SQLException {
        
        ResultSetMetaData mdat = rs.getMetaData();                           //Erstellt ein Objekt in der die Inf. gespeichert werden

        int sp = mdat.getColumnCount();
        
        StringBuilder z2 = new StringBuilder();                                                       //Erstellt den String z2
        String[] gstr = new String[mdat.getColumnCount()];                   //Erstellt den Array gstr & fÃ¼gt die Spalten Zahl ein
        for (int i = 0; i < mdat.getColumnCount(); i++) {
            z2.append("|%");
            z2.append(mdat.getColumnDisplaySize(i + 1));
            z2.append("s");                                                    //Erstellt einen String mit der Tab. formatierung
        }
        z2.append("%n");                                                    //ergÃ¤nzt noch das ende (optisch)
        String form = z2.toString();
        StringBuilder out = new StringBuilder();
        while (rs.next()) {
            for (int i = 1; i <= mdat.getColumnCount(); i++) {
                gstr[i - 1] = rs.getString(i);
            }
            out.append(String.format(form, gstr));
        }
        return out.toString();
    }
    
    private void rechnungAnz() {
        String RechnungsnummerIn = JOptionPane.showInputDialog("Rechnungsnummer Eingeben");
        RechnungOut(RechnungsnummerIn);
    }
    
    private void RechnungOut(String RechnungsnummerIn) {
        try {
            int rIn = Integer.parseInt(RechnungsnummerIn);
            Rechnung rino = Rechnung.getRechnung(DBTools.getCon(), rIn);
            System.out.println(rino);
            AusgabeWindow.setText(rino.toString());
        } catch (Exception E) {
            rechnungAnz();
        }
    }


